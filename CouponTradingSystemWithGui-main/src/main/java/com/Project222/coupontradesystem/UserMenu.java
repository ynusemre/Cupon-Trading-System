    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        if(coup !=null){
            Coupon selected;
            int i=0;
            int j = jComboBox2.getSelectedIndex();
            for (Iterator<Coupon> iterator = coup.iterator(); iterator.hasNext();) {
                Coupon next = iterator.next();
                if(j==i){
                    selected = next;
                    DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm");
                    LocalDateTime dateTime = LocalDateTime.parse("2021-07-02 05:15", formatter);

                    system.auction.createAuction(selected, user,dateTime );
                    break;
                }
                    
                i++;
            }
            new AuctionScreen(system, user).setVisible(true);
              
        }else{
            JOptionPane.showMessageDialog(null,"Please Select an item");
        }
        
        
        
        
    }//GEN-LAST:event_jButton8ActionPerformed

