    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        try{
            Coupon coupon;
            if(expireDate.getText().equals("") || couponName2.getText().equals("") ) throw new NullPointerException();
            if(Dollar.getText().equals("") || Cent.getText().equals("")){
                coupon = user.addNewCoupon(couponName2.getText(),expireDate.getText(), firmName.getText(),0,0);
            }else{
                coupon = user.addNewCoupon(couponName2.getText(),expireDate.getText(), firmName.getText(),Integer.parseInt(Dollar.getText()),Integer.parseInt(Cent.getText()));
            }
            
           
            
            if(coupon != null){
                JOptionPane.showMessageDialog(null,"Successful");
            }else{
                JOptionPane.showMessageDialog(null,"Unsuccessful");
            }
        }catch(NullPointerException e){
            JOptionPane.showMessageDialog(null,"Datas cant be blank");
        }catch(ArithmeticException e2){
            JOptionPane.showMessageDialog(null,"Password length must be at least 6");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

